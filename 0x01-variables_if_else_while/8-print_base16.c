#include <stdio.h>
#include <stdlib.h>

/**
 * Main - Entry point
 *
 * description: print all base of 16 in lowercase
 *
 * Return: Always 0; (success)
 */
int main(void)
{
	char c;

	int d;

	c = 'a';
	while
		(d < 10) {
			putchar (d + '0');
			d++;
		}
	while
		(c <= 'f') {
			putchar (c);
			c++;
		}
	putchar ('\n');
	return (0);
}
